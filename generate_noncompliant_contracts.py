    doc.add_heading(title, 0)
    for line in parties:
        doc.add_paragraph(line)
    for heading, paragraphs in clauses:
        doc.add_heading(heading, level=1)
        for paragraph in paragraphs:
            doc.add_paragraph(paragraph)
    doc.add_paragraph("甲方盖章：____________________")
    doc.add_paragraph("乙方签字：____________________")
    doc.add_paragraph("签署日期：2026年6月1日")
    doc.save(filename)
    print(filename)


